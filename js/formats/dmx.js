/**
 * DMX format descriptor.
 *
 * LEDBuild format for H803TC / H801RC controllers with DMX512 IC.
 * Magic: 00 00 48 43, controller count at offset 16-17 (uint16 LE).
 * Reversed port byte order: port N -> byte (7 - N).
 *
 * Header bytes extracted from known-good LEDBuild exports.
 */

// Sample headers (first 44 non-zero bytes) keyed by controller count.
const SAMPLE_HEADERS = {
  1: new Uint8Array([
    0x00, 0x00, 0x48, 0x43, 0x40, 0x40, 0x0a, 0x60,
    0x40, 0x66, 0x0a, 0x60, 0xe5, 0x41, 0x21, 0x12,
    0x01, 0x00, 0x45, 0x4d, 0xeb, 0xab, 0x3b, 0xf1,
    0x0e, 0xd9, 0xef, 0x60, 0x1e, 0xff, 0x71, 0xa3,
    0x1c, 0x00, 0x10, 0xc0, 0x49, 0x2d, 0xca, 0xc7,
    0x36, 0xb6, 0xa2, 0x55,
  ]),
  2: new Uint8Array([
    0x00, 0x00, 0x48, 0x43, 0x40, 0x40, 0x0a, 0x60,
    0x40, 0x0c, 0x0a, 0x60, 0xfd, 0x9d, 0x22, 0xd4,
    0x02, 0x00, 0x5c, 0x8d, 0xe4, 0x2b, 0x22, 0xb1,
    0x10, 0xf9, 0xed, 0x80, 0x1b, 0x7f, 0x61, 0x23,
    0x15, 0xc0, 0x1a, 0x60, 0x4f, 0x4d, 0xc8, 0xf5,
    0xaa, 0x08, 0x3d, 0x55,
  ]),
};

/** @type {import("./registry.js").FormatDescriptor} */
export default {
  name: "DMX",
  label: "DMX",

  controllerCountOffset: 16,
  controllerCountWidth: 2,

  buildHeader(controllerCount) {
    const sample = SAMPLE_HEADERS[controllerCount] || SAMPLE_HEADERS[1];
    const hdr = new Uint8Array(512);
    hdr.set(sample, 0);
    hdr[16] = controllerCount & 0xff;
    hdr[17] = (controllerCount >> 8) & 0xff;
    return hdr;
  },
};
