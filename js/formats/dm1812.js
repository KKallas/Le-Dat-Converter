/**
 * DM1812 format descriptor.
 *
 * LEDBuild format for H803TC / H801RC controllers with DM1812 IC.
 * Magic: 00 00 48 43, controller count at offset 16-17 (uint16 LE).
 * Reversed port byte order: port N -> byte (7 - N).
 *
 * Header bytes extracted from known-good LEDBuild exports.
 */

// Sample headers (first 44 non-zero bytes) keyed by controller count.
// Bytes 12-15 and 18-43 contain format-specific config + file hash.
const SAMPLE_HEADERS = {
  1: new Uint8Array([
    0x00, 0x00, 0x48, 0x43, 0x40, 0x40, 0x0a, 0x60,
    0x40, 0x66, 0x0a, 0x60, 0x17, 0x2c, 0x6c, 0x60,
    0x01, 0x00, 0x18, 0x6e, 0xfb, 0xab, 0x32, 0x91,
    0x19, 0x22, 0xf3, 0x20, 0x03, 0x3f, 0x6b, 0x63,
    0x12, 0x20, 0x09, 0x40, 0x03, 0x6e, 0xfb, 0xda,
    0x7f, 0x05, 0x96, 0x55,
  ]),
  2: new Uint8Array([
    0x00, 0x00, 0x48, 0x43, 0x40, 0x40, 0x0a, 0x60,
    0x40, 0x0c, 0x0a, 0x60, 0x19, 0x77, 0xdd, 0x58,
    0x02, 0x00, 0x0d, 0x6e, 0xfe, 0xab, 0x21, 0x31,
    0x1e, 0x02, 0xe8, 0xe0, 0x12, 0xff, 0x7b, 0xe3,
    0x0c, 0x20, 0x07, 0xe0, 0x1a, 0x6e, 0x8e, 0xaa,
    0x8b, 0xb9, 0x4a, 0x55,
  ]),
};

/** @type {import("./registry.js").FormatDescriptor} */
export default {
  name: "DM1812",
  label: "DM1812",

  controllerCountOffset: 16,
  controllerCountWidth: 2,

  buildHeader(controllerCount) {
    const sample = SAMPLE_HEADERS[controllerCount] || SAMPLE_HEADERS[1];
    const hdr = new Uint8Array(512);
    hdr.set(sample, 0);
    // Patch controller count at offset 16-17 (uint16 LE)
    hdr[16] = controllerCount & 0xff;
    hdr[17] = (controllerCount >> 8) & 0xff;
    return hdr;
  },
};
